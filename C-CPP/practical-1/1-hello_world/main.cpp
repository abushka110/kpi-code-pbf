// підклюення необхідної бібліотеки для операцій введення та виведення в C++.
#include <iostream>

int main() {
    // функція printf для виведення тексту (Hello world) в консолі, \n - символ нового рядка.
    printf("Hello World\n");
    // функція, яка очікує на введення користувача. Це дозволяє побачити вихідні дані до завершення роботи програми.
    getchar();
    // Повертає 0, щоб вказати успішне виконання програми.
    return 0;
}
